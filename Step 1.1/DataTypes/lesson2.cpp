#include <bits/stdc++.h>
using namespace std;

int main()
{
    cout << "Demonstration Of Floating Point DataTypes" << endl;
    float x = 1.01f;
    double y = 100.1;
    long double z =1234561.000002;

    cout << fixed << setprecision(2) << endl;
    cout << x  << endl;
    cout << y  << endl;
    cout << z  << endl;



    return 0;
}